package com.JavaProject1;

public class Emptystr {
	
	public static void main(String[] args) {
		
		String a="Hai";
		String b="How";
		String c="";
		String d=" ";
		String e=null;
		
		boolean em= a.isEmpty();
		System.out.println(em);
		
		boolean emp = b.isEmpty();
		System.out.println(emp);
		
		boolean empt = c.isEmpty();
		System.out.println(empt);
		
		boolean empty = d.isEmpty();
		System.out.println(empty);
		
		boolean empty2 = e.isEmpty();
		System.out.println(empty2);
		
		
	}

}
